<?php

function calSuma($uno, $dos, &$resu){
    $resu = $uno + $dos;
}

function calResta($uno, $dos, &$resu){
    $resu = $uno - $dos;
}

function calMulti($uno, $dos, &$resu){
    $resu = $uno * $dos;
}

function calDivi($uno, $dos, &$resu){
    $resu = $uno / $dos;
}

function calMod($uno, $dos, &$resu){
    $resu = $uno % $dos;
}

function calPot($uno, $dos, &$resu){
    $resu = $uno ** $dos;
}

?>