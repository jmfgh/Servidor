<?php

function calSuma($uno, $dos){
    $resu = $uno + $dos;
    return $resu;
}

function calResta($uno, $dos){
    $resu = $uno - $dos;
    return $resu;
}

function calMulti($uno, $dos){
    $resu = $uno * $dos;
    return $resu;
}

function calDivi($uno, $dos){
    $resu = $uno / $dos;
    return $resu;
}

function calMod($uno, $dos){
    $resu = $uno % $dos;
    return $resu;
}

function calPot($uno, $dos){
    $resu = $uno ** $dos;
    return $resu;
}

?>